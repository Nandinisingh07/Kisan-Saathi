// Function to translate the page based on the selected language
function applyTranslation(lang) {
    if (!translations[lang]) lang = 'hi'; // Default to Hindi
    
    // Find all elements with data-i18n attribute
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang][key]) {
            // Check if it's an input or placeholder
            if (el.tagName === 'INPUT' && el.hasAttribute('placeholder')) {
                el.setAttribute('placeholder', translations[lang][key]);
            } else {
                // If it's a bilingual container, change only the .hi-text child (the primary text label)
                const hiTextChild = el.querySelector('.hi-text');
                if (hiTextChild) {
                    hiTextChild.textContent = translations[lang][key];
                } else {
                    el.textContent = translations[lang][key];
                }
            }
        }
    });

    // Save language to localStorage
    localStorage.setItem('kisanLanguage', lang);
    
    // Dispatch custom event to notify specific page controllers
    document.dispatchEvent(new CustomEvent('languageChanged', { detail: lang }));
}

// Automatically apply selected language on load
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('kisanLanguage') || 'hi';
    
    // Bind change listener to language selector if it exists
    const selector = document.getElementById('language-selector');
    if (selector) {
        selector.value = savedLang;
        selector.addEventListener('change', (e) => {
            applyTranslation(e.target.value);
            if (typeof Toast !== 'undefined') {
                Toast.success("भाषा परिवर्तित की गई", "Language changed successfully");
            }
        });
    }
    
    applyTranslation(savedLang);
});
